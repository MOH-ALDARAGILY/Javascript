var radius = parseInt(prompt('Enter a circle radius'));
alert(`Area = ${Math.PI * Math.pow(radius, 2)}`);

var number = parseInt(prompt('Enter a number'));
alert(`SQRT = ${Math.sqrt(number)}`);

var angle = parseInt(prompt('Enter an angle'));
alert(`cos(${angle}) = ${(Math.cos(angle * Math.PI / 180)).toFixed(4)}`);

